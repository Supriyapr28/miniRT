#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int				len;
	int				i;
	unsigned char	*str;

	len = ft_strlen(s1);
	str = malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return ((char *)str);
}
